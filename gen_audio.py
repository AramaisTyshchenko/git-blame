async def generate_all():
    os.makedirs(AUDIO_DIR, exist_ok=True)
    for char_id, (text, voice) in PHRASES.items():
        path = os.path.join(AUDIO_DIR, f'{char_id}.mp3')
        if os.path.exists(path):
            print(f'  OK {char_id}: already exists ({os.path.getsize(path) // 1024} KB)')
            continue
        print(f'  >> {char_id}: generating ({voice})...', end=' ', flush=True)
        c = edge_tts.Communicate(text, voice)
        await c.save(path)
        size = os.path.getsize(path) // 1024
        print(f'done ({size} KB)')


if __name__ == '__main__':
    asyncio.run(generate_all())
    print('All audio generated!')
